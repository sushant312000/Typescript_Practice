"use strict";
class User {
    constructor(Id, email, first_name, last_name, avatar) {
        this.Id = 0;
        this.email = "";
        this.first_name = "";
        this.last_name = "";
        this.avatar = "";
        this.Id = Id;
        this.email = email;
        this.first_name = first_name;
        this.last_name = last_name;
        this.avatar = avatar;
    }
}
